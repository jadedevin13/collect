package org.openforis.collect.reporting.dto;

public class WasteTrackingDto {
    private String pointOfGenerationY;
    private String pointOfGenerationX;
    private String dateYear;
    private String dateMonth;
    private String dateDay;
    private String wasteType;
    private String subWasteType;
    private String wmStagingArea;
    private String amountManaged;
    private String units;
    private String wasteManagementFacility;
    private String comments;

    public String getPointOfGenerationY() {
        return pointOfGenerationY;
    }

    public void setPointOfGenerationY(String pointOfGenerationY) {
        this.pointOfGenerationY = pointOfGenerationY;
    }

    public String getPointOfGenerationX() {
        return pointOfGenerationX;
    }

    public void setPointOfGenerationX(String pointOfGenerationX) {
        this.pointOfGenerationX = pointOfGenerationX;
    }

    public String getDateYear() {
        return dateYear;
    }

    public void setDateYear(String dateYear) {
        this.dateYear = dateYear;
    }

    public String getDateMonth() {
        return dateMonth;
    }

    public void setDateMonth(String dateMonth) {
        this.dateMonth = dateMonth;
    }

    public String getDateDay() {
        return dateDay;
    }

    public void setDateDay(String dateDay) {
        this.dateDay = dateDay;
    }

    public String getWasteType() {
        return wasteType;
    }

    public void setWasteType(String wasteType) {
        this.wasteType = wasteType;
    }

    public String getSubWasteType() {
        return subWasteType;
    }

    public void setSubWasteType(String subWasteType) {
        this.subWasteType = subWasteType;
    }

    public String getWmStagingArea() {
        return wmStagingArea;
    }

    public void setWmStagingArea(String wmStagingArea) {
        this.wmStagingArea = wmStagingArea;
    }

    public String getAmountManaged() {
        return amountManaged;
    }

    public void setAmountManaged(String amountManaged) {
        this.amountManaged = amountManaged;
    }

    public String getUnits() {
        return units;
    }

    public void setUnits(String units) {
        this.units = units;
    }

    public String getWasteManagementFacility() {
        return wasteManagementFacility;
    }

    public void setWasteManagementFacility(String wasteManagementFacility) {
        this.wasteManagementFacility = wasteManagementFacility;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }
}
